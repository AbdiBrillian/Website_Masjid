<h2>Masjid Information</h2>
<p class="lead">Masjid Istiqlal (arti harfiah: Masjid Merdeka) adalah masjid nasional negara Republik Indonesia yang terletak di bekas Taman Wilhelmina, di Timur Laut Lapangan Medan Merdeka yang di tengahnya berdiri Monumen Nasional (Monas), di pusat ibukota Jakarta. Masjid ini merupakan salah satu dari 10 masjid terbesar kapasitasnya di dunia yang dapat menampung lebih dari 200.000 jamaah.
 Di seberang Timur masjid ini berdiri Gereja Katedral Jakarta. Imam besarnya adalah Prof. Dr. Nasaruddin Umar, M.A. dan Ketua Badan Pelaksana Pengelola Masjid Istiqlal sekarang adalah Laksma (Purn) H. Asep Saefuddin. <br> <br>

Pada 22 Februari 1955 diumumkan melalui surat kabar Sayembara Rencana Gambar Masjid Istiqlal. Ketua Panitia Sayembara ialah Mr. Assaat (mantan Presiden Negara Bagian RI yang berkedudukan di Yogyakarta, dulu Ketua Panitia Pembangunan Masjid Syuhada), dan Ketua Dewan Juri Presiden Republik Indonesia Ir. Soekarno.<br><br> Pemenang pertama sayembara adalah arsitek Frederich Silaban (seorang Kristen Protestan), memakai sandi “Ketuhanan”. Pemenang kedua adalah R. Oetoyo, memakai sandi “Istighfar”. Pemenang ketiga adalah Hans Groenewegen dengan sandi “Salam”. Pemenang keempat dan kelima, masing-masing lima orang Mahasiswa ITB, memakai sandi “Ilham”, dan tiga orang Mahasiswa ITB, memakai sandi “Khatulistiwa”. Dewan Juri memutuskan karya arsitek Frederich Silaban sebagai pemenang, dengan catatan gambar tersebut harus disempurnakan.

<br><br>Pembangunan masjid ini diprakarsai oleh Presiden Republik Indonesia saat itu, Ir. Soekarno. Peletakan batu pertama sebagai tanda dimulainya pembangunan Masjid Istiqlal dilakukan oleh Ir. Soekarno pada tanggal 24 Agustus 1961. Arsitek Masjid Istiqlal adalah Frederich Silaban, seorang Nasrani . 

<br><br>Masjid ini memiliki gaya arsitektur modern dengan dinding dan lantai berlapis marmer, dihiasi ornamen geometrik dari baja antikarat. Bangunan utama masjid ini terdiri dari lima lantai dan satu lantai dasar. Bangunan utama itu dimahkotai satu kubah besar berdiameter 45 meter yang ditopang 12 tiang besar. Menara tunggal setinggi total 96,66 meter menjulang di sudut Selatan selasar masjid. Masjid ini mampu menampung lebih dari 200.000 jamaah.</p>
